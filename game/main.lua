function love.update(dt)
  local items, len = world:getItems()
  for i = 1, len do
    items[i]:update(dt)
  end
end

function love.draw()
  love.graphics.setBackgroundColor(0.2, 0.2, 0.2)
  local items, len = world:getItems()
  for i = 1, len do
    items[i]:draw()
  end
end
